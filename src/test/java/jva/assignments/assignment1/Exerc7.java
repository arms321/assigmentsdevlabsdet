package jva.assignments.assignment1;

public class Exerc7 {

	//Write a program to print the sum of first 20 natural numbers.

	public static void main(String[] args) {
		
		int fNumber = 0;
		
		for(int i=fNumber;i<20;i++){
			fNumber += i;
		}
		
		System.out.println("Sum of first 20 natural numbers: "+fNumber);

	}
	
}
